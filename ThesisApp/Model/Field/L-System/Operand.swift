import Foundation

enum Operand: Character {
    case forward = "F"
    case right = "+"
    case left = "-"
    case up = "&"
    case down = "^"
    case rollLeft = "/"
    case rollRight = "\\"
    case startBranch = "["
    case endBranch = "]"
}
